"use client";

import { ReactNode, useState } from "react";

interface CyberTooltipProps {
  children: ReactNode;
  content: string;
  position?: "top" | "bottom" | "left" | "right";
}

export default function CyberTooltip({
  children,
  content,
  position = "top",
}: CyberTooltipProps) {
  const [visible, setVisible] = useState(false);

  const positionStyles = {
    top: "bottom-full left-1/2 -translate-x-1/2 mb-2",
    bottom: "top-full left-1/2 -translate-x-1/2 mt-2",
    left: "right-full top-1/2 -translate-y-1/2 mr-2",
    right: "left-full top-1/2 -translate-y-1/2 ml-2",
  };

  return (
    <div
      className="relative inline-flex"
      onMouseEnter={() => setVisible(true)}
      onMouseLeave={() => setVisible(false)}
    >
      {children}
      {visible && (
        <div
          className={`absolute ${positionStyles[position]} z-50 px-3 py-1.5 text-xs font-medium text-white bg-text-primary clip-btn whitespace-nowrap shadow-lg pointer-events-none`}
          role="tooltip"
        >
          {content}
        </div>
      )}
    </div>
  );
}
